DF <- read.table("household_power_consumption.txt",sep=";",header=TRUE)
DF[,1]<-as.Date(DF$Date,"%d/%m/%Y")
DF2<-subset(DF,DF[,1]=="2007-02-01")
DF3<-subset(DF,DF[,1]=="2007-02-02")
DF<-rbind(DF2,DF3)
hist(as.numeric(DF$Global_active_power),main="Global Active Power",col="red",xlab="Global Active Power (kilowatts)",xaxt='n')
axis(1,c(0,2,4,6),at=c(0,1000,2000,3000))
dev.copy(png,file="plot1.png",width=480,height=480)
dev.off()